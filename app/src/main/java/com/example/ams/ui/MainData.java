package com.example.ams.ui;

public class MainData {

    private String userCode;//用户名

    private String password;//密码

    private String InventoryUserName;//用户姓名

    public String getUserCode() {
        return userCode;
    }

    public void setUserCode(String userCode) {
        this.userCode = userCode;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getInventoryUserName() {
        return InventoryUserName;
    }

    public void setInventoryUserName(String inventoryUserName) {
        InventoryUserName = inventoryUserName;
    }


}
